package com.thinkgem.jeesite.modules.cms.entity;

import org.hibernate.validator.constraints.Length;

import com.thinkgem.jeesite.common.persistence.DataEntity;

/**
 * 产品案例Entity
 * @author xionghl
 * @version 2018-07-03
 */
public class ProductCase extends DataEntity<ProductCase> {
	
	private static final long serialVersionUID = 1L;
	private String title;		// 标题
	private String url;		// 连接
	private String image;		// 图片
	private String type;		// 所属板块
	
	public ProductCase() {
		super();
	}

	public ProductCase(String id){
		super(id);
	}

	@Length(min=0, max=64, message="标题长度必须介于 0 和 64 之间")
	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}
	
	@Length(min=0, max=64, message="连接长度必须介于 0 和 64 之间")
	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}
	
	@Length(min=0, max=255, message="图片长度必须介于 0 和 255 之间")
	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}
	
	@Length(min=1, max=1, message="所属板块长度必须介于 1 和 1 之间")
	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
}